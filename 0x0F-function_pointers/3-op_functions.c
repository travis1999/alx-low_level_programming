#include "3-calc.h"

/**
 * op_add - Adds two variables
 * @a: first int
 * @b: second int
 * Return: result for computation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Adds two variables
 * @a: first int
 * @b: second int
 * Return: result for computation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Adds two variables
 * @a: first int
 * @b: second int
 * Return: result for computation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Adds two variables
 * @a: first int
 * @b: second int
 * Return: result for computation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Adds two variables
 * @a: first int
 * @b: second int
 * Return: result for computation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
